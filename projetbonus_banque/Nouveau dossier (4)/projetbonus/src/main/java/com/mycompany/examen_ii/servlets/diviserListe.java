    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);

        UserDTO userDTO = null;
        List<UserDTO> users = new LinkedList<UserDTO>();
        List<UserDTO> vendeur = new LinkedList<UserDTO>();
        List<UserDTO> client = new LinkedList<UserDTO>();
        UserDAO userdao = new UserDAO();
        try {
            users = userdao.GetListe();
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }

        for (UserDTO niveau : users) {
            if (niveau.getNiveau() == "vendeur") {
                vendeur.add(niveau);

            } else {
                client.add(niveau);
            }
        }

        request.setAttribute("listeVendeur", vendeur);
        request.setAttribute("listeClient", client);
        response.sendRedirect(request.getContextPath() + "/Views/liste.jsp");
    }

